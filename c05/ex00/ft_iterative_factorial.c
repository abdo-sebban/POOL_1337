int	ft_iterative_factorial(int nb)
{
	int	i;
	int	k;

	i = 1;
	k = 1;
	if (nb < 0)
	{
		nb *= -1;
		k *= -1;
	}
	if (nb == 0 || nb == 1)
		i++;
	else
	{
		while (i <= nb)
		{
			k *= i;
			i++;
		}
	}
	return (k);
}
