int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	number;

	i = 0;
	number = 0;
	n = 1;
	while ((str[i] >= 9 && str[i] <= 13
			&& str[i] != '\0') || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] <= 57 && str[i] >= 48)
	{
		number *= 10;
		number += (str[i] - '0');
		i++;
	}
	return (number * n);
}
